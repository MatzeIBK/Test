

/* Timetable pressed */
function showTimeTable() {
    var x = document.getElementById("main");
    var y = document.getElementById("timeTable")
    x.style.display = 'none';
    y.style.display = true;
}

/* back button pressed */

function back_showHome() {
    var x = document.getElementById("main");
    x.style.display ="block";
}
